package com.xyc.proj.entity.statistic;

public class AlbumRevenueResultItem {
    private String albumId;

    private String albumTitle;

    private String artistTitle;

    private String albumPaymentCount;

    private String albumRevenue;

    private String trackPaymentCount;

    private String trackRevenue;

    private String totalRevenue;

    public String getAlbumId() {
        return albumId;
    }

    public void setAlbumId(String albumId) {
        this.albumId = albumId;
    }

    public String getAlbumTitle() {
        return albumTitle;
    }

    public void setAlbumTitle(String albumTitle) {
        this.albumTitle = albumTitle;
    }

    public String getArtistTitle() {
        return artistTitle;
    }

    public void setArtistTitle(String artistTitle) {
        this.artistTitle = artistTitle;
    }

    public String getAlbumPaymentCount() {
        return albumPaymentCount;
    }

    public void setAlbumPaymentCount(String albumPaymentCount) {
        this.albumPaymentCount = albumPaymentCount;
    }

    public String getAlbumRevenue() {
        return albumRevenue;
    }

    public void setAlbumRevenue(String albumRevenue) {
        this.albumRevenue = albumRevenue;
    }

    public String getTrackPaymentCount() {
        return trackPaymentCount;
    }

    public void setTrackPaymentCount(String trackPaymentCount) {
        this.trackPaymentCount = trackPaymentCount;
    }

    public String getTrackRevenue() {
        return trackRevenue;
    }

    public void setTrackRevenue(String trackRevenue) {
        this.trackRevenue = trackRevenue;
    }

    public String getTotalRevenue() {
        return totalRevenue;
    }

    public void setTotalRevenue(String totalRevenue) {
        this.totalRevenue = totalRevenue;
    }

}
